function Utf8Clean(txt)
	-- combos first:
	local combos = {
		[' Е'] = ' Ye',
		['^Е'] = 'Ye',
		[' е'] = ' ye',
		['^е'] = 'ye'
	};
	for i, v in pairs(combos) do
		txt = txt:gsub(i, v);
	end
	for i, v in pairs(UTF8Clean) do
		txt = txt:gsub(i, v);
	end
	return txt;
end

